module.exports.config = {
  name: "اقتباس",
  aliases: [],
  version: "2.0.0",
  hasPermssion: 0,
  credits: "Ayman ♛",
  description: "اقتباسات وحكم ملكية مع مكافآت من الخزينة",
  commandCategory: "utility",
  usages: " ",
  cooldowns: 2,
};

module.exports.run = async function({ api, event, args, Currencies }) {
  const { threadID, messageID, senderID } = event;
  const isTop = global.config.ADMINBOT.includes(senderID);

  const quotes = [
    "أحياناً.. يصبح الوهم حقيقة في عقل من فقد الأمل.",
    "الجمال يلفت الأنظار، لكن الطيبة هي التي تفتح القلوب.",
    "لا تحتقر صغائر الأمور، فإن الجبال العظيمة من الحصى الصغير.",
    "‏بعض الاعتذارات يجب أن تُرفض، لأنها تأتي بعد فوات الأوان.",
    "‏من ترك أمره لله، أعطاه الله فوق ما يتمنَّاه.",
    "‏إنعدام الرغبة أمام الشيء الذي أدمنته هو أعظم انتصار.",
    "فـ بالله صبر وبالله يسر وبالله عون وبالله كل شيء.",
    "البدايات للكل، والثبات فقط للصادقين.",
    "لا تظلم حتى لا تُظلم، فدعوة المظلوم ليس بينها وبين الله حجاب.",
    "كن لحوحاً في الدعاء، فقد أوشك السهم أن يصيب.",
    "الحياة مدرسة، والناس دروس، والزمن هو المعلم الحقيقي.",
    "عامل الناس بأخلاقك لا بأخلاقهم، فكل إناء بما فيه ينضح.",
    "النجاح ليس عدم السقوط، بل هو النهوض في كل مرة نسقط فيها.",
    "لا تقف أمام الريح فتكسرك، ولا تمشِ معها فتجرفك، كن أنت الريح.",
    "الصمت أحياناً يكون أبلغ من كل الكلمات التي قيلت والتي لم تُقل.",
    "أنا بعتز بنفسي جداً، واللي بيخسرني بيخسر أنظف وأجدع شخص ممكن يشوفه.",
    "فخورة بنفسي جداً رغم إني معملتش حاجة تستحق الذكر، لكنني كافحت."
  ];

  const randomQuote = quotes[Math.floor(Math.random() * quotes.length)];
  
  // نظام المكافأة الموحد (هدية من التوب عند القراءة)
  const reward = isTop ? 500 : 50; 
  await Currencies.increaseMoney(senderID, reward);

  const msg = `◈ ───『 حـكـمـة الـيـوم 』─── ◈\n\n` +
              `💬 "${randomQuote}"\n\n` +
              `💰 مكافأة القراءة: ${reward}$ (أضيفت لخزنتك)\n` +
              `◈ ───『 بـإدارة الـتـوب ايـمـن 👑 』─── ◈`;

  return api.sendMessage(msg, threadID, messageID);
};

module.exports.handleEvent = function ({ api, event }) {
  const { threadID, messageID, body } = event;
  if (!body || body.indexOf("كيف_اقتباس") != 0) return;

  const prefix = global.config.PREFIX;
  return api.sendMessage(`◈ ──『 مـعـلـومـات 』── ◈\n\n◯ الأمر: اقتباس\n◯ الوظيفة: يعطيك حكمة عشوائية ومكافأة مالية.\n◯ الاستخدام: ${prefix}اقتباس\n\n│←› الـمـديـر: الـتـوب ايـمـن 👑`, threadID, messageID);
};
