module.exports.config = {
  name: "لوخيروك",
  aliases: [],
  version: "2.0.0",
  hasPermssion: 0,
  credits: "Ayman ♛",
  description: "لعبة لو خيروك المطورة مع مكافآت الخزينة المركزية",
  commandCategory: "games",
  usages: " ",
  cooldowns: 2,
};

module.exports.run = async function({ api, event, Currencies }) {
  const { threadID, messageID, senderID } = event;
  const isTop = global.config.ADMINBOT.includes(senderID);

  // قائمة الأسئلة الضخمة
  const challenges = [
    "لو خيروك تكون عندك فلوس كثير وراسك حمار؟",
    "لو خيروك بين العيش وحدك في جزيرة منعزلة برفاهية أو في مكان قديم مع أصدقائك؟",
    "لو خيروك بين فقدان ذاكرتك والعيش مع أهلك أو بقاء ذاكرتك والعيش وحيداً؟",
    "لو خيروك بين تناول الخضار والفاكهة طوال حياتك أو تناول اللحوم فقط؟",
    "لو خيروك بين رؤية الأشباح فقط أو سماع صوتها فقط؟",
    "لو خيروك بين سماع أفكار الناس أو العودة بالزمن للخلف؟",
    "لو خيروك بين القدرة على الاختفاء أو القدرة على الطيران؟",
    "لو خيروك بين 5 ملايين دولار أو 5 ملايين لحظة سعادة حقيقية؟",
    "لو خيروك بين امتلاك سرعة الفهد أو دهاء الثعلب؟",
    "لو خيروك بين عمل براتب عالي لا تحبه أو عمل تحبه براتب ضعيف؟",
    "لو خيروك بين السفر لإيطاليا أو السفر لليابان؟",
    "لو خيروك أن تصبح بطلاً خارقاً سرياً أو مشهوراً جداً بلا قوى؟",
    "لو خيروك بين العيش في المستقبل المتطور أو العصر الأندلسي الذهبي؟",
    "لو خيروك بين الحب الحقيقي أو الثراء الفاحش؟",
    "لو خيروك أن تعيش بدون إنترنت لمدة سنة أو بدون أصدقاء لمدة سنة؟"
  ];

  const randomChallenge = challenges[Math.floor(Math.random() * challenges.length)];
  
  // مكافأة التفكير (نظام الخزينة المركزية)
  const reward = isTop ? 1000 : 100; // المدير يحصل على مكافأة أكبر
  await Currencies.increaseMoney(senderID, reward);

  const msg = `◈ ───『 لـو خـيـروك الـمـلكـية 』─── ◈\n\n` +
              `🤔 ${randomChallenge}\n\n` +
              `💰 مكافأة المشاركة: ${reward}$ (أضيفت لرصيدك الموحد)\n` +
              `◈ ───『 بـإدارة الـتـوب ايـمـن 👑 』─── ◈`;

  return api.sendMessage(msg, threadID, messageID);
};

module.exports.handleEvent = function ({ api, event }) {
  const { threadID, messageID, body } = event;
  if (!body || body.indexOf("كيف العب لو خيروك") != 0) return;

  return api.sendMessage(`◈ ──『 مـعـلـومـات 』── ◈\n\n◯ الأمر: لوخيروك\n◯ الوظيفة: يطرح عليك تحدي صعب ويمنحك مالاً.\n◯ الرسوم: مجانية حالياً.\n\n│←› الـمـدير: الـتـوب ايـمـن 👑`, threadID, messageID);
};
